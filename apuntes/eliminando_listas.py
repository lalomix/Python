my_list = [10, 8, 6, 4, 2]
del my_list[1:3]  # elimina desde el indice 1 hasta el 3 (sin contar el 3)
print(my_list)

my_list = [10, 8, 6, 4, 2]
del my_list[:]  # elimina el contenido de la lista
print(my_list)


